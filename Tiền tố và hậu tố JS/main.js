var a = 6;
++a;
var output = ++a;
console.log(++a); // a = 7

var a = 6;
--a;
var output = --a;
console.log(--a); // a = 5

var a = 6;
a++;
var output = a++;
console.log(output); // a = 6
console.log(a); // a = 7

var a = 6;
a--;
var output = a--;
console.log(output); // a = 6
console.log(a); // a = 5